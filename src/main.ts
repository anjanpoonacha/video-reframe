// --- Helpers ---

function getPositionAtTime(time: number, _duration: number): number {
  if (keyframes.length === 0) return 0.5;
  if (keyframes.length === 1) return keyframes[0]!.x;

  // Before first keyframe
  if (time <= keyframes[0]!.time) return keyframes[0]!.x;
  // After last keyframe
  if (time >= keyframes[keyframes.length - 1]!.time) return keyframes[keyframes.length - 1]!.x;

  // Find surrounding keyframes and interpolate
  for (let i = 0; i < keyframes.length - 1; i++) {
    const a = keyframes[i]!;
    const b = keyframes[i + 1]!;
    if (time >= a.time && time <= b.time) {
      const t = (time - a.time) / (b.time - a.time);
      return a.x * (1 - t) + b.x * t;
    }
  }
  return keyframes[keyframes.length - 1]!.x;
}

function recomputeFramePositions() {
  if (!videoEl) return;
  const duration = videoEl.duration;
  for (const f of frames) {
    f.x = getPositionAtTime(f.time, duration);
  }
}

function addOrUpdateKeyframe(time: number, x: number, auto = false) {
  // Find existing keyframe within 0.1s tolerance
  const existing = keyframes.find((k) => Math.abs(k.time - time) < 0.1);
  if (existing) {
    existing.x = x;
    existing.auto = auto;
  } else {
    keyframes.push({ time, x, auto });
    keyframes.sort((a, b) => a.time - b.time);
  }
  recomputeFramePositions();
  if (currentFile) saveSession(currentFile);
}

function removeKeyframe(time: number) {
  const idx = keyframes.findIndex((k) => Math.abs(k.time - time) < 0.1);
  if (idx !== -1 && keyframes.length > 1) {
    keyframes.splice(idx, 1);
    recomputeFramePositions();
    if (currentFile) saveSession(currentFile);
  }
}

async function detectMotion(
  video: HTMLVideoElement,
  n: number
): Promise<{ time: number; x: number }[]> {
  const pos: { time: number; x: number }[] = [];
  const step = video.duration / n;
  const dw = 160;
  const dh = Math.round(160 * (video.videoHeight / video.videoWidth));
  const c = document.createElement("canvas");
  c.width = dw;
  c.height = dh;
  const ctx = c.getContext("2d", { willReadFrequently: true })!;
  let prev: ImageData | null = null;

  for (let i = 0; i < n; i++) {
    video.currentTime = i * step;
    await new Promise((r) =>
      video.addEventListener("seeked", r, { once: true })
    );
    ctx.drawImage(video, 0, 0, dw, dh);
    const d = ctx.getImageData(0, 0, dw, dh);

    if (prev) {
      let sx = 0;
      let cnt = 0;
      const totalPixels = dw * dh;
      for (let y = 0; y < dh; y++) {
        for (let x = 0; x < dw; x++) {
          const idx = (y * dw + x) * 4;
          const diff =
            Math.abs((d.data[idx] ?? 0) - (prev.data[idx] ?? 0)) +
            Math.abs((d.data[idx + 1] ?? 0) - (prev.data[idx + 1] ?? 0)) +
            Math.abs((d.data[idx + 2] ?? 0) - (prev.data[idx + 2] ?? 0));
          if (diff > 60) {
            sx += x;
            cnt++;
          }
        }
      }
      const panThreshold = 0.6;
      const isPan = cnt / totalPixels > panThreshold;
      const lastGood = pos[pos.length - 1]?.x ?? 0.5;

      if (isPan) {
        // Whole-frame pan: centroid is meaningless, carry forward last good position
        pos.push({ time: i * step, x: lastGood });
      } else {
        pos.push({
          time: i * step,
          x: cnt > 50 ? sx / cnt / dw : lastGood,
        });
      }
    } else {
      pos.push({ time: 0, x: 0.5 });
    }

    prev = d;
    ($("analyzeProgress") as HTMLElement).style.width =
      Math.round((i / n) * 100) + "%";
  }
  return pos;
}

function smoothPositions(
  p: { time: number; x: number }[],
  w = 5
): { time: number; x: number }[] {
  return p.map((v, i) => {
    const s = Math.max(0, i - Math.floor(w / 2));
    const e = Math.min(p.length, i + Math.ceil(w / 2));
    return {
      time: v.time,
      x: p.slice(s, e).reduce((a, b) => a + b.x, 0) / (e - s),
    };
  });
}

function formatTime(s: number): string {
  const m = Math.floor(s / 60);
  const sec = (s % 60).toFixed(1);
  return `${m}:${sec.padStart(4, "0")}`;
}

// --- Play preview ---
let playing = false;

$("playPreviewBtn").addEventListener("click", async () => {
  if (playing) {
    playing = false;
    $("playPreviewBtn").textContent = "▶ Play";
    return;
  }

  playing = true;
  $("playPreviewBtn").textContent = "⏸ Stop";

  const startIdx = currentIdx;
  for (let i = startIdx; i < frames.length && playing; i++) {
    if (frames[i]!.skipped) continue;
    goToFrame(i);
    await drawCropEditor();
    // ~10fps playback
    await new Promise((r) => setTimeout(r, 100));
  }

  playing = false;
  $("playPreviewBtn").textContent = "▶ Play";
});

// --- Keyboard navigation ---
document.addEventListener("keydown", (e) => {
  if (!$("editorCard").classList.contains("hidden")) {
    if (e.key === "ArrowLeft") {
      let next = currentIdx - 1;
      while (next >= 0 && frames[next]!.skipped) next--;
      if (next >= 0) goToFrame(next);
    }
    if (e.key === "ArrowRight") {
      let next = currentIdx + 1;
      while (next < frames.length && frames[next]!.skipped) next++;
      if (next < frames.length) goToFrame(next);
    }
  }
});

