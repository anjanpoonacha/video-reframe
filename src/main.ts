// --- Types ---
interface FrameData {
  time: number;
  x: number; // 0-1 normalized crop position (computed from keyframes)
  bitmap: ImageBitmap;
  skipped: boolean;
}

interface Keyframe {
  time: number;
  x: number; // 0-1 normalized position
  auto: boolean; // true = generated by motion detection, false = user-placed
}

// --- State ---
let videoEl: HTMLVideoElement | null = null;
let frames: FrameData[] = [];
let keyframes: Keyframe[] = [];
let currentIdx = 0;
let skipRanges: { start: number; end: number }[] = [];
let markStartTime: number | null = null;
let dragging = false;

const $ = (id: string) => document.getElementById(id)!;

// --- Session persistence (IndexedDB for video, localStorage for edits) ---
interface SavedSession {
  fileName: string;
  fileSize: number;
  keyframes: Keyframe[];
  skipRanges: { start: number; end: number }[];
  numSamples: number;
}

const DB_NAME = "vr-storage";
const DB_STORE = "videos";

function openDB(): Promise<IDBDatabase> {
  return new Promise((resolve, reject) => {
    const req = indexedDB.open(DB_NAME, 1);
    req.onupgradeneeded = () => req.result.createObjectStore(DB_STORE);
    req.onsuccess = () => resolve(req.result);
    req.onerror = () => reject(req.error);
  });
}

async function saveVideoToDB(file: File): Promise<void> {
  const db = await openDB();
  const tx = db.transaction(DB_STORE, "readwrite");
  tx.objectStore(DB_STORE).put(file, "current-video");
  await new Promise<void>((resolve, reject) => {
    tx.oncomplete = () => resolve();
    tx.onerror = () => reject(tx.error);
  });
  db.close();
}

async function loadVideoFromDB(): Promise<File | null> {
  try {
    const db = await openDB();
    const tx = db.transaction(DB_STORE, "readonly");
    const req = tx.objectStore(DB_STORE).get("current-video");
    const result = await new Promise<File | null>((resolve) => {
      req.onsuccess = () => resolve(req.result ?? null);
      req.onerror = () => resolve(null);
    });
    db.close();
    return result;
  } catch { return null; }
}

async function clearVideoDB(): Promise<void> {
  try {
    const db = await openDB();
    const tx = db.transaction(DB_STORE, "readwrite");
    tx.objectStore(DB_STORE).delete("current-video");
    await new Promise<void>((resolve) => { tx.oncomplete = () => resolve(); });
    db.close();
  } catch { /* ignore */ }
}

function saveSession(file: File) {
  const session: SavedSession = {
    fileName: file.name,
    fileSize: file.size,
    keyframes,
    skipRanges,
    numSamples: frames.length,
  };
  localStorage.setItem("vr-session", JSON.stringify(session));
  saveVideoToDB(file);
}

function loadSession(): SavedSession | null {
  const raw = localStorage.getItem("vr-session");
  if (!raw) return null;
  try { return JSON.parse(raw); } catch { return null; }
}

function clearSession() {
  localStorage.removeItem("vr-session");
  clearVideoDB();
}

